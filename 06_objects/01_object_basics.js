// Javascript Object Basics

let firstName = 'Eren';
let age = 38;
let hobby = 'Kitap okumak';

const user = {
    firstName: 'Eren',
    age: 38,
    hobby: 'Kitap okumak',
};

console.log(user);
console.log(user.firstName);
console.log(user.age);
console.log(user.hobby);
console.log(user['firstName']);
console.log(user['age']);
console.log(user['hobby']);
