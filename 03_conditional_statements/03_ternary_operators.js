// Ternary Operators

let number1 = 10;
let number2 = 2;

// Uzun yoldan yapılması
let maxValue;

if (number1 > number2) {
    maxValue = number1;
} else {
    maxValue = number2;
}

console.log(maxValue);


// Tek satırla yapılması
let maxValue2 = number1 > number2 ? number1 : number2;
console.log(maxValue2);
