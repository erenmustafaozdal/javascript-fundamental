// Assignment Operators

let num1 = 10;
let num2 = num1;  // num2 = 10;

console.log(num1, num2);

num1 = num1 + 5;
console.log(num1);  // 15
num1 += 5;  // num1 = num1 + 5;
console.log(num1);  // 20

num1 = num1 - 5;
console.log(num1);  // 15
num1 -= 5;  // num1 = num1 - 5;
console.log(num1);  // 10

num1 = num1 * 5;
console.log(num1);  // 50
num1 *= 5;  // num1 = num1 * 5;
console.log(num1);  // 250
console.log(num1);  // 10

num1 = num1 / 5;
console.log(num1);  // 50
num1 /= 5;  // num1 = num1 / 5;
console.log(num1);  // 10

num1 = num1 + 1;
console.log(num1);  // 11
num1++;  // num1 = num1 + 1;
console.log(num1);  // 12

num1 = num1 - 1;
console.log(num1);  // 11
num1--;  // num1 = num1 - 1;
console.log(num1);  // 10
