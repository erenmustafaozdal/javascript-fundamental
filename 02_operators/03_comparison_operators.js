// Comparison Operators

/**
 * <: küçüktür
 * >: büyüktür
 * <=: küçük eşittir
 * >=: büyük eşittir
 * ==: eşittir (değer karşılaştırması)
 * ===: eşittir (değer ve tip karşılaştırması)
 * !=: eşit değildir (değer karşılaştırması)
 * !==: eşit değildir (değer ve tip karşılaştırması)
 */

let price = 200;

console.log(price > 300);  // false
console.log(price >= 200);  // true
console.log(price < 100);  // false
console.log(price <= 500);  // true
console.log(price == '200');  // true
console.log(price === '200');  // false
console.log(price != '200');  // false
console.log(price !== '200');  // true
